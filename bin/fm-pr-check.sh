#!/usr/bin/env bash
# Record a PR-ready task and arm firstmate's own PR poll.
#
# Appends pr=<url> and GitHub's pr_head=<sha> (when available) to state/<id>.meta,
# then writes state/<id>.check.sh, the watcher's slow poll for this task.
#
# The poll is firstmate's CI awareness: it reads the PR's merge state AND its check
# rollup straight from GitHub, so "still waiting on checks" versus "green, waiting
# on merge" versus "checks failed" is firstmate's own determination rather than a
# grep over some other tool's log prose.
#
# It honors the watcher check contract exactly (bin/fm-watch.sh's run_check):
# print ONE line only when firstmate should wake, print NOTHING otherwise, and
# finish well inside FM_CHECK_TIMEOUT (one bounded `gh pr view` call).
#
# Wake on:
#   - the PR is MERGED         -> "merged"        (teardown time)
#   - a check has FAILED       -> "checks failed" (firstmate must act)
# Stay silent while:
#   - checks are still running/queued (the crew or CI is mid-flight)
#   - checks are green but the PR is not merged (waiting on the captain's merge)
#   - the repo reports no checks at all (nothing to fail)
#   - gh is missing, unauthenticated, or the call fails (never wake on a tool error)
#
# A failure wakes once per PR head: the failing head sha is recorded in
# state/<id>.ci-seen, so a persistent red check does not re-wake firstmate every
# poll, while a new push (a fix round that fails again) does.
# Usage: fm-pr-check.sh <task-id> <pr-url>
set -eu

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
FM_ROOT="${FM_ROOT_OVERRIDE:-$(cd "$SCRIPT_DIR/.." && pwd)}"
FM_HOME="${FM_HOME:-${FM_ROOT_OVERRIDE:-$FM_ROOT}}"
STATE="${FM_STATE_OVERRIDE:-$FM_HOME/state}"
"$FM_ROOT/bin/fm-guard.sh" || true
ID=$1
URL=$2

META="$STATE/$ID.meta"
if [ -f "$META" ]; then
  WT=$(grep '^worktree=' "$META" | tail -1 | cut -d= -f2- || true)
  PR_HEAD=
  if [ -n "$WT" ] && [ -d "$WT" ]; then
    if command -v gh >/dev/null 2>&1; then
      if REMOTE_HEAD=$(cd "$WT" && gh pr view "$URL" --json headRefOid -q .headRefOid 2>/dev/null); then
        PR_HEAD=$REMOTE_HEAD
      fi
    fi
  fi
  if ! grep -qxF "pr=$URL" "$META"; then
    echo "pr=$URL" >> "$META"
  fi
  if [ -n "$PR_HEAD" ] && ! grep -qxF "pr_head=$PR_HEAD" "$META"; then
    echo "pr_head=$PR_HEAD" >> "$META"
  fi
fi

# The rollup verdict is computed by gh's own jq-syntax -q expression, so the poll
# needs no external jq and makes exactly one API call. Entries are either CheckRun
# (status QUEUED/IN_PROGRESS/COMPLETED + conclusion) or StatusContext (state), so
# each is normalized to f=<conclusion|state> and s=<status> before the verdict:
#   fail    - any terminal red/blocked conclusion
#   pending - anything still queued, running, or awaiting a status context
#   pass    - everything completed and none red
#   none    - the repo reports no checks on this PR
# CANCELLED/SKIPPED/NEUTRAL are deliberately NOT failures: a cancelled superseded
# run is routine concurrency, and waking on it would be a false alarm.
ROLLUP_QUERY='
  [ .state,
    (.headRefOid // "none"),
    ( (.statusCheckRollup // [])
      | map({ f: (.conclusion // .state // ""), s: (.status // "") })
      | if length == 0 then "none"
        elif any(.[]; .f == "FAILURE" or .f == "TIMED_OUT" or .f == "ACTION_REQUIRED" or .f == "STARTUP_FAILURE" or .f == "ERROR") then "fail"
        elif any(.[]; (.s != "" and .s != "COMPLETED") or .f == "PENDING" or .f == "EXPECTED" or .f == "") then "pending"
        else "pass" end )
  ] | join(" ")'

cat > "$STATE/$ID.check.sh" <<EOF
# Generated by fm-pr-check.sh for task $ID. Watcher check contract: print one line
# only when firstmate should wake, print nothing otherwise.
set -u
URL='$URL'
SEEN='$STATE/$ID.ci-seen'
command -v gh >/dev/null 2>&1 || exit 0
line=\$(gh pr view "\$URL" --json state,headRefOid,statusCheckRollup -q '$ROLLUP_QUERY' 2>/dev/null) || exit 0
read -r pr_state pr_head checks <<< "\$line"
[ -n "\${pr_state:-}" ] || exit 0

if [ "\$pr_state" = MERGED ]; then
  echo "merged"
  exit 0
fi

[ "\${checks:-}" = fail ] || exit 0
sig="\${pr_head:-none} fail"
if [ -f "\$SEEN" ] && [ "\$(cat "\$SEEN" 2>/dev/null)" = "\$sig" ]; then
  exit 0
fi
printf '%s\n' "\$sig" > "\$SEEN"
echo "checks failed: \$URL"
EOF
echo "armed: state/$ID.check.sh polls $URL (merged or failed checks)"
